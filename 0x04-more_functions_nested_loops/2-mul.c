#include <ctype.h>
/**
 * mul - this function check if an input is a digit or not
 * @a: the first input argument
 * @b: the second input argument
 * Description: to check if a character is upper case or not
 *
 * Return: an integer value as outputs
 */

int mul(int a, int b)
{
	int res;

	res = a * b;

	return (res);
}
